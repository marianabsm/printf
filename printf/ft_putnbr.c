#include "ft_printf.h"

int	ft_putnbr(int n)
{
	long long int	nb;
	int				i;

	nb = n;
	i = 0;
	if (n < 0)
	{
		i += ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		i += ft_putnbr(nb / 10);
		i += ft_putnbr(nb % 10);
	}
	else
		i += ft_putchar(nb + 48);
	return (i);
}
